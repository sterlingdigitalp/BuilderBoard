//! Tool execution results and artifacts.

use serde::{Deserialize, Serialize};

/// Result of a single tool execution.
#[derive(Clone, Debug, Serialize, Deserialize)]
pub struct ToolResult {
    pub success: bool,
    pub exit_code: Option<i32>,
    pub output: ToolOutput,
    pub artifacts: Vec<ToolArtifact>,
    pub review_items: Vec<ReviewItem>,
}

/// Output produced by a tool (structured, not raw).
#[derive(Clone, Debug, Serialize, Deserialize)]
pub struct ToolOutput {
    pub stdout: String,
    pub stderr: String,
    pub summary: String,
}

impl ToolOutput {
    pub fn new(stdout: String, stderr: String, summary: String) -> Self {
        Self {
            stdout,
            stderr,
            summary,
        }
    }
}

/// A structured artifact produced by a tool.
#[derive(Clone, Debug, Serialize, Deserialize)]
pub struct ToolArtifact {
    pub artifact_type: String,
    pub summary: String,
    pub content: Option<String>,
    pub path: Option<String>,
    pub mime_type: Option<String>,
}

/// A review item generated by a mutating tool operation.
#[derive(Clone, Debug, Serialize, Deserialize)]
pub struct ReviewItem {
    pub action: String,
    pub summary: String,
    pub details: Option<String>,
    pub severity: String, // "info" | "warning" | "critical"
}

/// Descriptor returned by Tool::describe()
#[derive(Clone, Debug, Serialize, Deserialize)]
pub struct ToolDescriptor {
    pub id: String,
    pub display_name: String,
    pub description: String,
    pub category: String,
    pub permissions: Vec<String>,
    pub supported_engines: Vec<String>,
    pub supported_execution_classes: Vec<String>,
}
